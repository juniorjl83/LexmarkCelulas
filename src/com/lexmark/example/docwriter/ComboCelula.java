package com.lexmark.example.docwriter;

public class ComboCelula
{

   String name;
   String pid;
   
   public ComboCelula(String name, String pid)
   {
      super();
      this.name = name;
      this.pid = pid;
   }
   public String getName()
   {
      return name;
   }
   public void setName(String name)
   {
      this.name = name;
   }
   public String getPid()
   {
      return pid;
   }
   public void setPid(String pid)
   {
      this.pid = pid;
   }
   
   
   
}
